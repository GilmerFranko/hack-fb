function generateEmail(name) {
	let seed = 0;
	for (let i = 0; i < name.length; i++) {
		seed += name.charCodeAt(i);
	}
	let randomNum = seed % 10000;
	let emailName = name.toLowerCase().split(' ').join('_');
	return emailName + randomNum + '@gmail.com';
}
